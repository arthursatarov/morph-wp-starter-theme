			<h3>Выбор цветовой палитры</h3>

			<p>При создании цветовой схемы важно учитывать не только эстетические предпочтения, но и психологическое воздействие цветов, а также их влияние на читаемость текста.</p>

			<h4>Основные принципы</h4>

			<p>Выбирайте цвета осознанно. Каждый цвет должен иметь свою функцию в дизайн-системе. Например, красный традиционно используется для ошибок, зелёный — для успешных действий, синий — для информационных сообщений.</p>

			<h2>Адаптивная типографика</h2>

			<p>Современные веб-сайты должны корректно отображаться на устройствах с различными размерами экранов. <mark>Адаптивная типографика</mark> позволяет тексту масштабироваться плавно в зависимости от ширины вьюпорта.</p>

			<pre><code>/* Пример использования clamp() */
.text {
  font-size: clamp(1rem, 0.95rem + 0.25vw, 1.125rem);
}</code></pre>

			<p>Функция <code>clamp()</code> принимает три параметра: минимальное значение, предпочтительное значение и максимальное значение. Это позволяет создавать плавно масштабируемые размеры шрифтов.</p>

			<h3>Viewport-based units</h3>

			<p>Использование единиц измерения на основе вьюпорта (<code>vw</code>, <code>vh</code>, <code>vmin</code>, <code>vmax</code>) даёт дополнительную гибкость, но требует осторожности, чтобы избежать слишком мелкого или крупного текста на экстремальных размерах экранов.</p>

